function applyBackgroundImages() {
  const heroBg = document.querySelector('.hero-bg');
  if (heroBg) heroBg.style.backgroundImage = `url('${TMS_IMAGES.hero}')`;

  document.querySelectorAll('.page-header').forEach(el => {
    el.style.setProperty('--page-header-bg', `url('${TMS_IMAGES.pageHeader}')`);
  });
}

document.addEventListener('DOMContentLoaded', applyBackgroundImages);
